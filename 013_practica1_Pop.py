# Definimos la lista de colores
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja']

# Usamos pop() para eliminar el color en la posición 1 y almacenarlo en 'Almacena_valor'
Almacena_valor = colores.pop(1)

# Usamos pop() para eliminar el penúltimo color (índice -2) y almacenarlo en 'Almacena_valor2'
Almacena_valor2 = colores.pop(-2)

# Imprimimos los colores eliminados y almacenados
print('Los colores eliminados y almacenados son:', Almacena_valor, "y", Almacena_valor2)
