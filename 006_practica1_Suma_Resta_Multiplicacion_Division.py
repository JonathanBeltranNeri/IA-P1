## suma, resta, multiplicación y división
# Sumamos varios números y almacenamos el resultado en 'varsuma'
varsuma = 20 + 23 + 35 + 9
print(varsuma)  # Salida: 87

# Realizamos una operación de suma y resta en 'verrest'
verrest = 20 + 23 - 80 - 50
print(verrest)  # Salida: -87

# Multiplicamos tres valores y almacenamos el resultado en 'varmult'
varmult = 20 * 23 * 1.89
print(varmult)  # Salida: 869.4

# Realizamos una operación de división en 'verdiv'
verdiv = 5000 / 230 / 2.17
print(verdiv)  # Salida: aproximadamente 10.14

# Realizamos una operación combinada de división, suma, resta y multiplicación en 'verfinal'
verfinal = 10 / 5 + 15 - 17 * 1
print(verfinal)  # Salida: -14.0
