# Ordenamiento de elementos en listas Python

# Se define una lista con colores.
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón',
'lila', 'negro', 'rosa', 'blanco', 'naranja']

# Se ordena la lista en orden descendente usando el método sort() con reverse=True.
colores.sort(reverse=True)

# Se imprime la lista ordenada de forma descendente.
print(colores)