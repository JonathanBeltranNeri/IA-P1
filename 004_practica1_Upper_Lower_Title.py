## upper(), lower() y title() 
# Definimos una variable con un nombre en minúsculas
nombre = "jonathan beltran neri"

# Convertimos la primera letra de cada palabra en mayúscula usando title()
nombre = nombre.title()
print(nombre)  # Salida: Jonathan Beltran Neri

# Definimos una variable con una frase con diferentes capitalizaciones
frasel = "Esta Es Una Frase Para Ser Formateada."

# Convertimos toda la frase a minúsculas usando lower()
frasel = frasel.lower()
print(frasel)  # Salida: esta es una frase para ser formateada.

# Definimos otra variable con la misma frase
fraseU = "Esta Es Una Frase Para Ser Formateada."

# Convertimos toda la frase a mayúsculas usando upper()
fraseU = fraseU.upper()
print(fraseU)  # Salida: ESTA ES UNA FRASE PARA SER FORMATEADA.
