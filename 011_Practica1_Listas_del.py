# Eliminar elementos de listas en Python

# Se define una lista con colores.
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja']
del colores[4] # Elimina 'marrón' (en la posición 4)
del colores[1] # Elimina 'azul' (en la posición 1)
del colores[-4]# Elimina 'rosa' (en la posición -4)
del colores[-3]# Elimina 'blanco' (en la posición -3)

# Imprime la lista 'colores' después de eliminar los elementos.
print(colores)
