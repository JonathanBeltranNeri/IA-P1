# Los métodos upper(), lower() y title de Python

# Convierte la primera letra de cada palabra en mayúscula usando title().
nom="jonathan beltran neri"
nom=nom.title()
# Imprime el nombre con la primera letra de cada palabra en mayúscula.
print(nom)

# Convierte todo el texto a minúsculas usando lower().
mensaje1="Esta Es Una Frase Para Ser Formateada."
mensaje1=mensaje1.lower()
# Imprime el texto en minúsculas.
print(mensaje1)

# Convierte todo el texto a mayúsculas usando upper().
mensaje2="Esta Es Una Frase Para Ser Formateada."
mensaje2=mensaje2.upper()
# Imprime el texto en mayúsculas.
print(mensaje2)