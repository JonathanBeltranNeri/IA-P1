##El condicional IF ELSE de Python
# Definimos la variable color
color = "rojo"

# Verificamos si el valor de color es igual a "rojo"
if color == "rojo":
    print("El color es rojo.")  # Se ejecutará ya que color es "rojo"
else:
    print("El color no es rojo")  # No se ejecutará porque color es "rojo"
