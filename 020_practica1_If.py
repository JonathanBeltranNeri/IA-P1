##El condicional IF de Python
# Definimos dos variables num1 y num2
num1 = 15
num2 = 20

# Verificamos si num1 no es igual a num2
if num1 != num2:
    print('Se ejecuta el if.')  # Se ejecutará ya que 15 no es igual a 20

