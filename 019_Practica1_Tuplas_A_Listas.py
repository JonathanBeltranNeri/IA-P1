# Cómo convertir tuplas a listas y viceversa en PythoN

# Se define una lista con diferentes nombres de colores.
colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja']

# Se convierte la lista en una tupla utilizando la función tuple().
tupla = tuple(colores)

# Se imprime el tipo de dato de la variable 'tupla' para verificar que es una tupla.
print(type(tupla)) # Salida: <class 'tuple'>