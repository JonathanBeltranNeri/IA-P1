# El condicional IF de Python y los operadores de comparación

# Se asignan valores a las variables num1 y num2
num1 = 15
num2 = 20
# Se evalúa si num1 es diferente a num2.
# Como son diferentes, se ejecuta el bloque de código dentro del if.
if num1 != num2:
	print('Se ejecuta el if.')


# Se cambian los valores de num1 y num2
num1 = 1450
num2 = 60
# Se evalúa si num1 es mayor que num2.
# Como 1450 es mayor que 60, se ejecuta el bloque de código dentro del if.
if num1 > num2:
	print('Se ejecuta el if.')
	

# Se cambian nuevamente los valores de num1 y num2
num1 = 1450
num2 = 1450
# Se evalúa si num1 es diferente de num2.
# Como num1 es igual a num2, el bloque de código dentro del if no se ejecuta
if num1 != num2:
	print('Se ejecuta el if.')