# El condicional IF ELSE de Python

# Se asigna el valor "rojo" a la variable color
color = "rojo"

# Se evalúa si el valor de la variable color es igual a "rojo".
# Como la condición es verdadera, se ejecuta el bloque de código dentro del if.
if color == "rojo" :
    print("El color es rojo")
else :
    # Si la condición fuera falsa, se ejecutaría este bloque.
    print("El color no es rojo")
