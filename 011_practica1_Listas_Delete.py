## LISTAS DELETE
# Definimos una lista de colores
colores = ["rojos", "azul", "verde", "amarillo", "marron", "lila", "negro", "rosa", "blanco", "naranja"]

# Eliminamos el elemento en la posición 1 (índice 1, que es "azul")
del colores[1]

# Eliminamos el elemento en la posición 4 (índice 4, que es "marron") después de eliminar "azul"
del colores[4]

# Eliminamos el tercer elemento desde el final (índice -3, que es "negro")
del colores[-3]

# Eliminamos el cuarto elemento desde el final (índice -4, que es "lila")
del colores[-4]

# Imprimimos la lista después de las eliminaciones
print(colores)  # Salida: ['rojos', 'verde', 'amarillo', 'lila', 'rosa', 'blanco', 'naranja']
