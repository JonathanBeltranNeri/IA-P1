# El bucle while de Python con ejemplos

x=0
# Primer bucle while que imprime números desde 0 hasta 15, incrementando de 5 en 5
while x <= 15:
	print(x)
	x += 5 # Aumenta x en 5 en cada iteración
	
# Segundo bucle while que imprime números desde el valor de x hasta -100, disminuyendo de 20 en 20
while x >= -100:
	print(x)
	x -=20# Disminuye x en 20 en cada iteración

x=10
# Tercer bucle while que imprime el valor de x desde 10 hasta 0, disminuyendo de 1 en 1
while x >= 0:
	print('El valor del bucle es:',x)
	x -= 1# Disminuye x en 1 en cada iteración
