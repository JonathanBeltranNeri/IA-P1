## CONCATENAR
# Concatenamos dos cadenas sin espacio entre ellas
var1 = "Hola" + "bienvenido"
print(var1)  # Salida: Holabienvenido

# Definimos dos variables con cadenas de texto
var2 = "Hola"
var3 = "bienvenido"

# Concatenamos las variables agregando un espacio en medio
var4 = var2 + " " + var3
print(var4)  # Salida: Hola bienvenido

# Definimos variables para un nombre y apellidos
varnom = "Jonathan"
varapellido = "Beltran"
varapellido2 = "Neri"

# Concatenamos las variables con espacios entre ellas
varfinal = varnom + " " + varapellido + " " + varapellido2
print(varfinal)  # Salida: Jonathan Beltran Neri

# Definimos variables con números en formato de cadena (string)
varnum1 = "15"
varnum2 = "98"

# Concatenamos las cadenas en lugar de sumarlas numéricamente
print(varnum1 + varnum2)  # Salida: 1598 (ya que se trata de strings y no números)
